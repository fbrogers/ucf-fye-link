	<p>
		LINK events are not just fun and entertaining &ndash; they're profitable! Listed
		on the LINK website are all the LINK-associated events. For 
		each program a student attends (and signs-in or slides their UCF ID at), they
		will receive LINK Loot. Students can collect LINK Loot points until the end 
		of each semester (Summer, Fall, and Spring).
	</p>

	<h2>How Can I Get It?</h2>
	<p>
		To receive LINK Loot, students must attend the programs listed on the website
		and register their attendance. After the program, the program presenter 
		will forward the attendance sheet to the LINK office and marked students will be
		credited with LINK Loot. You don't have to search very hard to find a 
		LINK program because there is practically one occurring every day &ndash; just
		check our <a href="programs">list of programs</a>!
	</p>

	<h2>Where Can I Use It?</h2>
	<p>
		Students can use their accrued LINK Loot at each end-of-semester event
		(the <em>LINK Lottery</em> in the Summer and the <em>LINK Auction</em> in the Spring).
		Prizes at both events typically include Blu-Ray players, TVs,
		laptops, movies, video games, digital cameras, gift certificates, event tickets,
		UCF Spirit items, and more! There will even be a chance to win a $1,500 tuition scholarship
		from SGA by participating in LINK events during the Fall term! LINK points expire at the
		end of Summer, then roll over from Fall to Spring.
	</p>

	<h3>The LINK Lottery (Summer)</h3>	
	<p>
		The LINK Lottery is a raffle-style event where students trade LINK Loot for
		tickets that then can be put toward items of their choosing! A ticket is 
		chosen randomly and the owner of that ticket wins the prize! Students can
		distribute their tickets to many prizes or put them all towards one!
	</p>
	
	<h3>SGA Tuition Scholarship (Fall)</h3>	
	<p>
		The Fall term brings with it the chance to win a $1,500 tuition scholarship
		provided by SGA. Students that have accrued over 10,000 LINK points will be put into
		a drawing, and a winner will be randomly selected at the end of the term. Students'
		LINK Loot does not get used up for being put in the raffle, and they will continue to
		have those points when the Spring term begins.
	</p>
	
	<h3>The LINK Auction (Spring)</h3>	
	<p>
		The LINK Auction is held at the end of the Spring semester in the
		<a href="http://map.ucf.edu/locations/52/student-union-stun/">Student Union's</a> 
		Pegasus Ballroom. This is the chance for students to compete for prizes by bidding
		with the LINK Loot they have been accumulating during the Fall and Spring semesters.
	</p>
	
	<h2>How Many Points Do I Have?</h2>	
	<p>
		Students are encouraged to check their credited LINK Loot via the
		<a href="loot">My Loot</a> page. Programs are typically credited to student
		accounts after five business days. If students notice a program is missing
		after this time-frame, they can access a "LINK Loot Dispute Form" on
		the My Loot page.
	</p>
