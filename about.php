<div class="left">
	<p>
		LINK (Learning and Interacting with New Knights) is an education and involvement based program to help
		first-time-in-college (FTIC) students become engaged with the campus. Developed by
		<a href="http://www.sdes.ucf.edu/">Student Development and Enrollment Services</a> (SDES)
		and located in the office of <a href="http://fye.sdes.ucf.edu/">First Year Experience</a> (FYE),
		LINK is designed to get students out of their rooms and into the UCF community. 
		With events from academic and learning programs to interaction and community-building events,
		there is something to help every student feel connected to their new university home. 
	</p>
	<p>
		The main focus of LINK is to get new Knights involved with the campus and its many resources
		and programs, to meet new people, and to have fun along the way! Through LINK, students attend
		programs and events on campus worth various amounts of LINK Loot, earn Loot all semester, and
		cash in their <a href="linkloot">Loot</a> for fabulous prizes at the LINK Lottery in the summer
		and the LINK Auction in the Spring. Weâ€™re here to help students get involved and connected to
		their campus, and to learn how to become the Ultimate Knight!
	</p>
	
	<h2>Why LINK?</h2>
	<p>
		Coming to college can be overwhelming and a bit confusing for many students, especially
		when students come to a university with 60,000 students! We know that becoming involved and
		informed of resources can be daunting, which is why LINK was created. LINK provides an easy way
		for students to begin exploring all that UCF has to offer while creating connections
		that are vital to their success.
	</p>
	
	<h2 id="theme">Becoming the <em>Ultimate Knight</em></h2>
	<p>
		At Orientation, students learn how to become <em>The Ultimate Knight</em> and LINK helps carry
		on this theme by sponsoring workshops and events that relate to the tenets
		of <em>The Ultimate Knight</em>:
	</p>
	<ul>
		<li>
			<p>
				<strong>Get Involved</strong> helps students to learn about all of the awesome
				student organization and involvement opportunities on campus.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Smart</strong> teaches students the skills needed to succeed academically
				and connects students to the resources that can support them throughout
				their UCF experience.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Diversified</strong> helps students learn about the rich UCF
				campus culture and the many experiences fellow students will bring to the table.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Real</strong> teaches about integrity and ethical decision making,
				equipping students to handle difficult situations that come their way.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Savvy</strong> helps students learn how to manage finances, apply
				for scholarships, and understand the FAFSA process.
			</p>
		</li>
		<li>
			<p>
				<strong>Get to Work</strong> shows students how to find a major, connect to
				career goals, and find opportunities to enhance their resumes while in college.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Healthy</strong> helps identify all of the resources available for
				students related to physical, emotional, spiritual, and sexual health.
			</p>
		</li>
		<li>
			<p>
				<strong>Get Global</strong> shows how students are involved in the community,
				from developing sustainability initiatives to volunteering on campus
				and in the community.
			</p>
		</li>
		<li>
			<p>
				<strong>Get It All Together</strong> brings together all of the other
				Ultimate Knight components and shows students how they can apply this knowledge
				after successfully completing their first year.
			</p>
		</li>
	</ul>
